class Swapping{

	public static void main(String args[]){

	int a = 2, b = 3, temp;

	temp = a;

	a = b;

	b = temp;

	System.out.println("\nAfter swapping of \"a\" :"+a);

	System.out.println("\nAfter swapping of \"b\" :"+b);
	}
}

/* Output :

After swapping of "a" :3

After swapping of "b" :2

*/